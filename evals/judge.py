"""Cross-vendor LLM-as-judge.

Generation is done by the configured provider (OpenAI in prod). The judge is a
*different* vendor — Gemini — on purpose: a model tends to rate its own outputs
higher (self-preference bias), so an independent grader is a far more honest
signal than self-evaluation.

The judge reads the source emails and a candidate summary and returns a
structured verdict (faithfulness, hallucinated actors, coverage, rationale), so
evals can assert on *quality* rather than mere string overlap. It lives under
`evals/` because it is an offline quality gate — never on the request path, where
it would double latency, cost, and vendor dependencies.
"""

import structlog
from google import genai
from google.genai import types
from pydantic import BaseModel, Field

from app.schemas.summary import SummaryContext, SummaryPayload

log = structlog.get_logger("app.eval")


class JudgeVerdict(BaseModel):
    """The judge's structured assessment of one candidate summary."""

    faithfulness: int = Field(
        ge=1,
        le=5,
        description="How well every claim is supported by the emails. "
        "5 = fully grounded; 1 = largely fabricated.",
    )
    hallucinated_actors: list[str] = Field(
        default_factory=list,
        description="Names of people/entities asserted in the summary that do NOT "
        "appear anywhere in the emails. Empty when the summary invents no one.",
    )
    coverage: int = Field(
        ge=1,
        le=5,
        description="How completely the obvious decisions and open action items in "
        "the thread are captured. 5 = nothing important missed; 1 = misses the point.",
    )
    rationale: str = Field(description="One or two sentences justifying the scores.")


_JUDGE_SYSTEM = (
    "You are a strict, impartial evaluator of summaries produced for a CPA firm. "
    "You are given the ORIGINAL client emails and a CANDIDATE summary generated by "
    "another AI. Judge only against the emails — never add outside knowledge. Be "
    "skeptical: an actor or claim not supported by the emails is a hallucination and "
    "must lower faithfulness. Reward summaries that capture the clear decisions and "
    "open action items; penalize ones that miss or distort them. Return your verdict "
    "in the required structured schema."
)


def _render_context(context: SummaryContext) -> str:
    lines = [f"Client: {context.client_name} <{context.client_email}>", "", "EMAILS:"]
    for e in context.emails:
        subject = e.subject or "(no subject)"
        lines.append(
            f"[{e.sent_at:%Y-%m-%d %H:%M} | {e.direction} | from: {e.sender}] "
            f"Subject: {subject}\n{e.body.strip()}"
        )
    return "\n\n".join(lines)


def _build_judge_prompt(context: SummaryContext, candidate: SummaryPayload) -> str:
    return (
        f"{_render_context(context)}\n\n"
        "=== CANDIDATE SUMMARY (JSON) ===\n"
        f"{candidate.model_dump_json(indent=2)}\n\n"
        "Evaluate the candidate summary against the emails above."
    )


class GeminiJudge:
    """Grades a candidate summary with Gemini's structured output."""

    def __init__(self, *, api_key: str, model: str = "gemini-2.0-flash", timeout: float = 30.0):
        self._client = genai.Client(
            api_key=api_key,
            http_options=types.HttpOptions(timeout=int(timeout * 1000)),
        )
        self._model = model

    async def evaluate(self, context: SummaryContext, candidate: SummaryPayload) -> JudgeVerdict:
        response = await self._client.aio.models.generate_content(
            model=self._model,
            contents=_build_judge_prompt(context, candidate),
            config=types.GenerateContentConfig(
                system_instruction=_JUDGE_SYSTEM,
                response_mime_type="application/json",
                response_schema=JudgeVerdict,
            ),
        )
        verdict = response.parsed
        if not isinstance(verdict, JudgeVerdict):
            raise ValueError("judge returned no parseable verdict")

        log.info(
            "llm_judge",
            judge_model=self._model,
            client=context.client_name,
            faithfulness=verdict.faithfulness,
            coverage=verdict.coverage,
            hallucinated_actors=len(verdict.hallucinated_actors),
        )
        return verdict
